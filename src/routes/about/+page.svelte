<h1>About the SHACL Murder Mystery</h1>
<p>The SPARQL Murder Mystery was created by <a href="https://mark.lindeman.nu">Mark Lindeman</a>. 
The SPARQL based game is a adaption of the <a href="https://mystery.knightlab.com/"><em>SQL Murder Mystery</em></a>, 
which by itself was inspired by <a href="https://github.com/veltman/clmystery"><em>a crime in the neighboring Terminal City</em></a>. 
The detective illustrations are provided by <a href="https://www.vecteezy.com/free-vector/detective.">Detective Vectors by Vecteezy</a>.
This project is released under the <a href="https://commission.europa.eu/content/european-union-public-licence_en">European Union Public Licence v.1.2 license</a>.
</p>
